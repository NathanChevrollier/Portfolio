        <!-- Difficultés et solutions -->
        <h3 class="titre-section mt-4">Difficultés rencontrées & solutions</h3>
        <span class="titre-barre"></span>
        <div class="row">
          <div class="col-12">
            <table class="table table-bordered">
              <thead class="table-dark">
                <tr>
                  <th>Problème</th>
                  <th>Solution apportée</th>
                </tr>
              </thead>
              <tbody>
                <tr>
                  <td>
                    <strong>Gestion multi-profils (client, restaurateur, admin)</strong><br>
                    Difficulté à séparer clairement les fonctionnalités par rôle et sécuriser les accès
                  </td>
                  <td>
                    <ul>
                      <li>Création de middlewares spécifiques à chaque rôle (AdminMiddleware, RestaurateurMiddleware, ClientMiddleware)</li>
                      <li>Séparation des routes par middleware et groupe</li>
                      <li>Implémentation d'une logique de vérification dans chaque contrôleur avec des méthodes de vérification (`isAdmin()`, `isRestaurateur()`, etc.)</li>
                      <li>Utilisation des Gates Laravel pour les vérifications d'autorisation plus complexes</li>
                    </ul>
                  </td>
                </tr>
                <tr>
                  <td>
                    <strong>Gestion des réservations en temps réel</strong><br>
                    Problèmes de conflit lors de réservations simultanées de tables
                  </td>
                  <td>
                    <ul>
                      <li>Utilisation d'AJAX pour vérifier la disponibilité des tables sans rechargement de page</li>
                      <li>Implémentation d'un système de vérification en deux temps (pré-vérification côté client, confirmation côté serveur)</li>
                      <li>Utilisation de transactions de base de données pour éviter les conflits</li>
                      <li>Mise en place d'un délai de réservation pour éviter les doubles réservations</li>
                    </ul>
                  </td>
                </tr>
                <tr>
                  <td>
                    <strong>Gestion des relations complexes entre entités</strong><br>
                    Difficultés avec les relations entre restaurants, catégories, plats, commandes et réservations
                  </td>
                  <td>
                    <ul>
                      <li>Modélisation précise avec des relations Eloquent (ORM de Laravel)</li>
                      <li>Utilisation de relations polymorphiques quand nécessaire</li>
                      <li>Optimisation des requêtes avec le eager loading (`with()`) pour éviter le problème N+1</li>
                      <li>Tests approfondis des différentes relations avec des cas d'utilisation réels</li>
                    </ul>
                  </td>
                </tr>
                <tr>
                  <td>
                    <strong>Sécurisation des accès et des données</strong><br>
                    Protection contre les accès non autorisés et validation des données
                  </td>
                  <td>
                    <ul>
                      <li>Validation systématique des entrées utilisateur avec les FormRequest de Laravel</li>
                      <li>Vérifications d'appartenance (ex: un restaurateur ne peut modifier que ses propres restaurants)</li>
                      <li>Protection CSRF sur tous les formulaires</li>
                      <li>Authentification robuste avec hashage des mots de passe</li>
                      <li>Limitation des tentatives de connexion pour prévenir les attaques par force brute</li>
                    </ul>
                  </td>
                </tr>
                <tr>
                  <td>
                    <strong>Déploiement et configuration serveur</strong><br>
                    Complexité de la mise en production avec les migrations et configurations
                  </td>
                  <td>
                    <ul>
                      <li>Configuration d'un pipeline CI/CD avec GitHub Actions</li>
                      <li>Création de scripts de déploiement automatisés</li>
                      <li>Utilisation de variables d'environnement pour les configurations sensibles</li>
                      <li>Documentation détaillée du processus de déploiement</li>
                      <li>Tests automatisés avant déploiement</li>
                    </ul>
                  </td>
                </tr>
              </tbody>
            </table>
